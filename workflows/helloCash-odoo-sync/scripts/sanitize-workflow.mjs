#!/usr/bin/env node
/**
 * Sanitize a workflow JSON exported from n8n for safe git storage.
 *
 * Removes instance-specific fields that cause noisy diffs and credential
 * ID mismatches when the workflow is imported into another n8n instance.
 *
 * Usage:
 *   node scripts/sanitize-workflow.mjs < exported.json > src/workflow-template.json
 *   node scripts/sanitize-workflow.mjs exported.json   > src/workflow-template.json
 *   node scripts/sanitize-workflow.mjs exported.json     src/workflow-template.json
 *
 * What is stripped:
 *   - wf.id, wf.meta and other top-level API-noise (active, tags, pinData, version*, …)
 *   - settings keys not allowed on public API (e.g. binaryMode)
 *   - node.credentials[*].id              (credential DB ID — differs per instance)
 *   - node.parameters.jsCode              (replaced by build.mjs from src/nodes/)
 *   - node.position                       (layout noise — regenerated by n8n on import)
 *
 * What is kept:
 *   - node.credentials[*].name            (used by n8n to match on import)
 *   - name, nodes, connections, settings (safe subset), staticData
 */

const TOP_LEVEL_STRIP = new Set([
  'id',
  'active',
  'activeVersion',
  'activeVersionId',
  'createdAt',
  'updatedAt',
  'isArchived',
  'shared',
  'tags',
  'pinData',
  'triggerCount',
  'versionCounter',
  'versionId',
  'description',
  'meta',
]);

const ALLOWED_SETTINGS_KEYS = new Set([
  'executionOrder',
  'saveManualExecutions',
  'callerPolicy',
  'errorWorkflow',
  'timezone',
]);

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

// ---------------------------------------------------------------------------
// Input
// ---------------------------------------------------------------------------
const [, , arg1, arg2] = process.argv;

let raw;
if (arg1 && arg1 !== '-') {
  const inputPath = path.resolve(arg1);
  if (!fs.existsSync(inputPath)) {
    console.error(`sanitize-workflow: file not found: ${inputPath}`);
    process.exit(1);
  }
  raw = fs.readFileSync(inputPath, 'utf8');
} else {
  // read from stdin
  raw = fs.readFileSync('/dev/stdin', 'utf8');
}

// ---------------------------------------------------------------------------
// Parse
// ---------------------------------------------------------------------------
let wf;
try {
  wf = JSON.parse(raw);
} catch (e) {
  console.error('sanitize-workflow: invalid JSON —', e instanceof Error ? e.message : e);
  process.exit(1);
}

if (!Array.isArray(wf.nodes) || typeof wf.connections !== 'object') {
  console.error('sanitize-workflow: does not look like an n8n workflow (missing nodes[] or connections)');
  process.exit(1);
}

// ---------------------------------------------------------------------------
// Strip top-level fields rejected by public POST /api/v1/workflows
// ---------------------------------------------------------------------------
for (const k of TOP_LEVEL_STRIP) {
  delete wf[k];
}

// ---------------------------------------------------------------------------
// settings: only keys accepted by public API
// ---------------------------------------------------------------------------
if (wf.settings != null && typeof wf.settings === 'object' && !Array.isArray(wf.settings)) {
  for (const k of Object.keys(wf.settings)) {
    if (!ALLOWED_SETTINGS_KEYS.has(k)) {
      delete wf.settings[k];
    }
  }
}

// ---------------------------------------------------------------------------
// Sanitize nodes
// ---------------------------------------------------------------------------
const CODE_NODE_TYPE = 'n8n-nodes-base.code';

for (const node of wf.nodes) {
  // 1. Credential IDs — keep name so n8n can prompt for rebind on import
  if (node.credentials && typeof node.credentials === 'object') {
    for (const credKey of Object.keys(node.credentials)) {
      if (node.credentials[credKey] && typeof node.credentials[credKey] === 'object') {
        delete node.credentials[credKey].id;
      }
    }
    // Drop credentials object entirely if it is now empty
    if (Object.keys(node.credentials).length === 0) {
      delete node.credentials;
    }
  }

  // 2. jsCode — managed by build.mjs from src/nodes/; strip to keep template lean
  if (node.type === CODE_NODE_TYPE && node.parameters?.jsCode !== undefined) {
    delete node.parameters.jsCode;
  }

  // 3. Node position — layout noise, not logic
  delete node.position;
}

// ---------------------------------------------------------------------------
// Output
// ---------------------------------------------------------------------------
const out = JSON.stringify(wf, null, 2) + '\n';

if (arg2) {
  const outPath = path.resolve(arg2);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, out, 'utf8');
  console.error('sanitize-workflow: wrote', path.relative(process.cwd(), outPath));
} else {
  process.stdout.write(out);
}
